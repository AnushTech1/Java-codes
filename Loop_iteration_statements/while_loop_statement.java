package Loop_iteration_statements;

public class while_loop_statement {
    public static void main(String[] args){
        //While loop ka use ek condition ke true hote rahne tak kisi define code block ko 
        //execute kerne ke liey kia jata hai.
        int num = 10;
        while(num >= 1){
            System.out.println("hello " + num);
            num--;
        }
    }
}
