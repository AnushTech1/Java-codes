package Math_methods;

public class Sqrt_Cbrt_method {
    public static void main(String[] args) {
        //sqrt() method ka use squre root nikalne ke liey kia jata hai.
        double num1 = Math.sqrt(16);
        System.out.println(num1);//output : 4
            //or direct
        System.out.println(Math.sqrt(25));// output : 5

        //cqrt() method ka use cube root nikalne ke liey kia jata hai.
        double num2 = Math.cbrt(8);
        System.out.println(num2);//output : 2
            //or direct
        System.out.println(Math.cbrt(125));// output : 5
    }
}
