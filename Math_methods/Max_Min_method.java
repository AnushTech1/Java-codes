package Math_methods;
public class Max_Min_method{
    public static void main(String[] args){
        //max() method ka use two integer value mese maximum value find kerne ke liye hote hai.
        int num = Math.max(5, 10);
        System.out.println(num);//output : 10

        //min() method ka use two integer value mese minimum value find kerne ke liye hote hai.
        int num1 = Math.min(5, 10);
        System.out.println(num1);//output : 5
    }
}