package Math_methods;

public class Abs_Pow_method {
 public static void main(String[] args) {
    //abs() method ka use absolute value prapt kerne ke liye kia jata hai.
    int num1 = Math.abs(-5);
    System.out.println(num1);//output : 5

    //pow() method ka use power apply kerne ke liye kia jata hai.
    double num2 = Math.pow(2,3);//is method ke liey double datatype ka use hota hai.
    System.out.println(num2);//output : 8.0
 }    
}
