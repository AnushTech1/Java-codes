package Data_types;

public class Float_type {
    public static void main(String[] args) {
        
        // Float data type ka use floating value store kerne ke liey kia jata hai.
        // Float or double me value ke last me f or d aata hai.
        float num1 = 2.50f; // f denotes this is a float value
        float num2 = 2.50f;
        float total = num1 + num2;
        System.out.println(total);
    }
    
}
