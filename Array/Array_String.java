public class Array_String {
    public static void main(String[] args) {
        // This is a normal array example program
        // String array
        String[] courses = {
            "Java",
            "Python",
            "C++",
            "C#",
            "JavaScript"
        };
        // Print the array elements using an enhanced for loop
        for (String course : courses) {
            System.out.println(course);
        }
    }
}
