package Operators;

public class Arithmetic_operators {
public static void main(String[] args) {
    
    //Arithmetic operators means -, + , * , / 
    int num1 = 10, 
        num2 = 5;
    int add = num1 + num2 ,
        sub = num1 - num2 , 
        mul = num1 * num2 , 
        div = num1 / num2;

    System.out.println(add);
    System.out.println(sub);
    System.out.println(mul);
    System.out.println(div);
}    
}
